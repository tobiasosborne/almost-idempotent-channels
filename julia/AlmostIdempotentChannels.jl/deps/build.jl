# build.jl — produces deps.jl with the absolute path to build/libaic.so.
#
# Mirrors ../su2-fft/julia/deps/build.jl. Strategy: shell out to
# `make -C <repo_root> lib` (the Makefile's `lib` target -> build/libaic.so,
# which globs all src/*.c so the aic_ucp_shim flat-double ABI is auto-included).
# Then write the absolute resolved .so path to deps.jl so
# AlmostIdempotentChannels.jl can `include` it.
#
# PATH OFFSET (load-bearing): this package lives TWO levels deep inside the repo
# (julia/AlmostIdempotentChannels.jl/), so deps/build.jl is three levels below
# the repo root. su2-fft's package sits one level deep (julia/), hence its
# `joinpath(@__DIR__, "..", "..")`; ours needs one more "..".
#
# Bead: aic-m24, increment 2b.

const REPO_ROOT  = abspath(joinpath(@__DIR__, "..", "..", ".."))
const SO_PATH    = joinpath(REPO_ROOT, "build", "libaic.so")
const DEPS_FILE  = joinpath(@__DIR__, "deps.jl")

println("[AlmostIdempotentChannels build.jl] make -C $REPO_ROOT lib")

run(`make -C $REPO_ROOT lib`)

isfile(SO_PATH) || error("Build failed: $SO_PATH not found after `make lib`")

open(DEPS_FILE, "w") do io
    println(io, "# Generated by build.jl — do not edit by hand.")
    println(io, "const LIBAIC = ", repr(SO_PATH))
end

println("[AlmostIdempotentChannels build.jl] Wrote $DEPS_FILE pointing at $SO_PATH")
